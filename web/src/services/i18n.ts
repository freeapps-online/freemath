function getBrowserLanguage(): string {
  if (typeof navigator === 'undefined') return 'en-US'
  return navigator.languages?.[0] ?? navigator.language ?? 'en-US'
}

export function resolveLanguage(preference: LanguagePreference): SupportedLanguage {
  const target = preference === 'system' ? getBrowserLanguage() : preference
  if ((SUPPORTED_LANGUAGES as readonly string[]).includes(target)) return target as SupportedLanguage

  const base = target.toLowerCase().split('-')[0]
  const match = SUPPORTED_LANGUAGES.find((language) => language.toLowerCase().split('-')[0] === base)
  return match ?? 'en-US'
}

export function getStrings(language: SupportedLanguage): LocaleStrings {
  return TRANSLATIONS[language]
}

export function getLanguageName(code: LanguagePreference, displayLanguage: SupportedLanguage): string {
  if (code === 'system') return getStrings(displayLanguage).preferences.system
  try {
    const display = new Intl.DisplayNames([displayLanguage], { type: 'language' })
    return display.of(code) ?? code
  } catch {
    return code
  }
}

export function getLanguageFlag(code: LanguagePreference): string {
  if (code === 'system') return '🌐'
  return LANGUAGE_META[code].flag
}

export function getLanguageNativeLabel(code: LanguagePreference, displayLanguage: SupportedLanguage): string {
  if (code === 'system') return getStrings(displayLanguage).preferences.system
  return LANGUAGE_META[code].nativeLabel
}

export function normalizeTranscript(text: string): string {
  return text
    .normalize('NFKC')
    .toLowerCase()
    .replace(/[.,!?;:()[\]{}]/g, ' ')
    .replace(/\s+/g, ' ')
    .trim()
}

export function detectSideCommand(text: string, language: SupportedLanguage): AnswerSide | null {
  const normalized = normalizeTranscript(text)
  if (!normalized) return null
  const strings = getStrings(language)

  for (const token of strings.sideWords.left) {
    if (normalized.includes(token.toLowerCase())) return 'left'
  }
  for (const token of strings.sideWords.right) {
    if (normalized.includes(token.toLowerCase())) return 'right'
  }

  return null
}

export function getLevelLabel(level: number, language: SupportedLanguage): string {
  return getStrings(language).levelLabels[level] ?? EN_LEVELS[level] ?? `Level ${level}`
}
